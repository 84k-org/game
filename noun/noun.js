function rupa (n, vibhatti) {
    if (vibhatti.charAt(0) == "-")
        return n.substring(0,n.length-1) + vibhatti.charAt(1) + n.substr(n.length-1,1) + vibhatti.substring(2,vibhatti.length);
    if (vibhatti=="X")
        return "-";
    return n + vibhatti;
}

