function ans (i, j) {

    if (!$("#Key" + i + "" + j).hasClass("ui-icon-carat-r"))
        return;

    $("#Key" + i + "" + j).text(n_vibhatti_ans[i][j]);
    $("#Key" + i + "" + j).removeClass("ui-icon-carat-r ui-alt-icon");


    if (n_vibhatti_ans_boo[i][j]) {     // CORRECT ANSWER


        $("#Key" + i + "" + j).addClass("RightBttn ui-icon-check");

        switch (mode_group) {

            case 1:
                total_right_count++;
                if (--ans_count==0)     ProducePassBttn();
                break;

            case 0:
                $("#right_count").text (++total_right_count);
                if (--ans_count==0)     ProducePassBttn();
                break;

            case 2:
                //$("#right_count").text (++total_right_count);
                total_right_count++;
                $("#Fan").text("ถูกต้องนะครับ!");
                $("#Fan").css("color","green");
                $("#Fan").fadeIn(0).fadeOut(1000);

                if (--ans_count==0)     n_vibhatti_init();
                else
                    Q_timer = jQuery.now()+(FanCountDown*1000);

                break;
        }


        return;
    }


    $("#Key" + i + "" + j).addClass("WrongBttn ui-icon-delete");

    switch (mode_group) {
        case 1:
            total_wrong_count++;
            break;
        case 0:
            $("#wrong_count").text (++total_wrong_count);
            break;
        case 2:
            //$("#wrong_count").text (++total_wrong_count);
            $("#Fan").text("ผิดครับ!");
            $("#Fan").css({"color":"red"});
            $("#Fan").fadeIn(0).fadeOut(1000);

            total_wrong_count++;
            n_vibhatti_init();
            break;
    }
}

